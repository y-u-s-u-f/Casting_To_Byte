package com.yusuf;

public class toByte {

		public static void main(String[] args) {
			
			byte short_to_byte = (byte)Short.MAX_VALUE;
			byte int_to_byte = (byte)Integer.MAX_VALUE;
			byte long_to_byte = (byte)Long.MAX_VALUE;
			byte float_to_byte = (byte)Float.MAX_VALUE;
			byte double_to_byte = (byte)Double.MAX_VALUE;
			
            System.out.println("Short to Byte = " + short_to_byte );	
            System.out.println("Int to Byte = " + int_to_byte );
            System.out.println("Long to Byte = " + long_to_byte );
            System.out.println("Float to Byte = " + float_to_byte );
            System.out.println("Double to Byte = " + double_to_byte );
		}


}
